                        <p>This month’s message comes from John Maxwell. Every company will go through what I like to call ‘growth stages’. How you manage these distinct stages can determine the overall level of success that your company will achieve. I have personally experienced these stages both at a corporate level and as a small business owner. </p>
                        <h6>Growth Stages of a Company</h6>
                        <p>There are four distinct growth stages within any organization. The key is to understand where your business is, and in that awareness, take steps to avoid stage four… </p>
                        <h6>Stage One: Entrepreneurial Stage</h6>
                        <p>Here the business seeks ‘viability’. You must have a viable product and/or service to have a successful company. During stage one, you must be quick on your feet, agile and able to adapt to your business climate as you fine-tune your value proposition.</p>
                        <h6>Stage Two: Emerging Stage</h6>
                        <p>Once you have established your viable product and/or service, the second stage is considered the emerging stage. This is where you must create and gain ‘credibility’ through building a high level of trust between you and your customers. Mike Krzyzewski basketball coach at Duke once said, “In leadership there is no word more important than trust.</p>
                        <h6>Stage Three: Stability Stage</h6>
                        <p>The ‘stability’ stage is where you establish your organization as a force in the market with a sustainable business plan. Customers create stability. No matter what business you are in, you need to create an environment that will drive customer loyalty. The dealers that survived the automotive downturn all had one thing in common, solid fixed operations that were create by a loyal customer base. </p>
                        <ul class="">
                            <li>So...</li>
                            <li>Stage One = <strong>Viability</strong></li>
                            <li>Stage Two = <strong>Credibility</strong> </li>
                            <li>Stage Three = <strong>Stability</strong></li>
                        </ul>
                        <h6>Stage Four: Erosion Stage </h6>
                        <p>This stage has taken down many businesses over the years and should be recognized and avoided at all costs. The fourth and final stage, if allowed, is ‘erosion’. Erosion occurs when a company drifts away from their foundation, their core strength, and the single attribute that drove its success. </p>

                        <p>Erosion is a stage marked by vulnerability. Unless the organization returns to their roots and re-engages with their core values, they could crumble from within. We have seen this scenario from automobile manufactures to dealer services companies. You must return to the foundation, the core competency that drove your success. Be it service, people or product; don’t stray too far away from your ‘wheel house’. This does not mean that a company should lack innovation. It means that you utilize visionary forward thinking and modern technology to build on the core values that made you successful.</p>

                        <p>Too many times an organization may achieve great success only to watch in evaporate. This happens when a company culture is slowly changed from a “customer-focused driven” company to a “revenue-focused driven” one. Focus on the customer and revenue will follow. Change is good, change is necessary, but changing your foundation, your core values may be a risky proposition. </p>

